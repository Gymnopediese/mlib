#include "mlib.h"

void	m_add(t_mtx *a, t_mtx *b, t_mtx *res)
{
	int		x;
	int		y;

	x = -1;
	while (++x < a->size)
	{
		y = -1;
		while (++y < a->arr[0].size)
		{
			res->arr[x].arr[y] = a->arr[x].arr[y] + b->arr[x].arr[y];
		}
	}
}
